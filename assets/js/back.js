function claro (){
    const fundo = document.getElementById('fundoSite')
        fundo.addEventListener('click' , fundo.style.backgroundColor ='#fffcf2', 
            fundo.style.color='#000000'
        )
}
function escuro(){
    const fundo = document.getElementById('fundoSite')
        fundo.addEventListener('click' , fundo.style.backgroundColor ='#1e1b18' ,
            fundo.style.color ='#fffaff'
        )
}